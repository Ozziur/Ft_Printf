#include "ft_printf.h"

char	*ft_strchr(const char *s, int c)
{
	unsigned char	ch;

	ch = (unsigned char) c;
	while ((*s != '\0') && (*s != ch))
	{
		s++;
	}
	if (*s == ch)
		return ((char *)s);
	return ((char *) NULL);
}

void	ft_putchar(char ch, t_flag *flag)
{
	write(1, &ch, 1);
	flag->counter++;
}
